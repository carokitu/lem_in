int		list_flag(char c)
{
	char	*flags;
	int		i;

	i = 0;
	flags = "0123456789.#+- hlL|";
	while (flags[i] != c && flags[i] != '|')
		i++;
	if (flags[i] == c && c != '|')
		return (1);
	return (0);
}
